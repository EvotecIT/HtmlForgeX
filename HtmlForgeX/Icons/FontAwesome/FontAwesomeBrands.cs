/// <summary>
/// Extension methods for FontAwesomeBrands enum
/// </summary>
public static class FontAwesomeBrandsExtensions {
    /// <summary>
    /// Gets the Unicode character code for the FontAwesome brand icon
    /// </summary>
    public static string GetCode(this FontAwesomeBrands icon) {
        var field = icon.GetType().GetField(icon.ToString());
        if (field != null) {
            var attribute = (DescriptionAttribute?)Attribute.GetCustomAttribute(field, typeof(DescriptionAttribute));
            if (attribute != null) {
                return attribute.Description;
            }
        }
        return string.Empty;
    }
}