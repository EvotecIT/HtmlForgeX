namespace HtmlForgeX;

public struct Color {
    public byte A { get; private set; }
    public byte R { get; private set; }
    public byte G { get; private set; }
    public byte B { get; private set; }

    // Private constructor to set color components
    private Color(byte a, byte r, byte g, byte b) : this() {
        A = a;
        R = r;
        G = g;
        B = b;
    }

    // FromArgb with alpha value
    public static Color FromArgb(int alpha, int red, int green, int blue) {
        return new Color((byte)alpha, (byte)red, (byte)green, (byte)blue);
    }

    // FromArgb without explicit alpha value (assumes full opacity)
    public static Color FromArgb(int red, int green, int blue) {
        return new Color(255, (byte)red, (byte)green, (byte)blue);
    }

    public override string ToString() {
        return $"Color [A={A}, R={R}, G={G}, B={B}]";
    }
}


