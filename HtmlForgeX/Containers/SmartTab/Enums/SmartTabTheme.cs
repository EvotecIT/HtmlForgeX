namespace HtmlForgeX;

/// <summary>
/// Represents the theme options available for SmartTab.
/// </summary>
public enum SmartTabTheme {
    /// <summary>Basic theme with minimal styling.</summary>
    Basic,
    /// <summary>Classic theme with traditional tab styling.</summary>
    Classic,
    /// <summary>Dark theme for dark mode interfaces.</summary>
    Dark,
    /// <summary>Bootstrap theme for Bootstrap-based layouts.</summary>
    Bootstrap,
    /// <summary>Square theme with square edges.</summary>
    Square,
    /// <summary>Round theme with rounded edges.</summary>
    Round,
    /// <summary>Material design theme.</summary>
    Material
}