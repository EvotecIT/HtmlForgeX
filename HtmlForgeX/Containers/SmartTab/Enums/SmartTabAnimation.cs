namespace HtmlForgeX;

/// <summary>
/// Represents the animation types available for SmartTab transitions.
/// </summary>
public enum SmartTabAnimation {
    /// <summary>No animation.</summary>
    None,
    /// <summary>Fade animation.</summary>
    Fade,
    /// <summary>Horizontal slide animation.</summary>
    SlideHorizontal,
    /// <summary>Vertical slide animation.</summary>
    SlideVertical,
    /// <summary>Slide with swing effect.</summary>
    SlideSwing,
    /// <summary>Custom CSS animation.</summary>
    Css
}